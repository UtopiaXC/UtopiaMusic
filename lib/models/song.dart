class Song {
  final String title;
  final String artist;
  final String coverUrl;
  final String lyrics;
  final int colorValue;
  final String bvid;
  final int cid;

  const Song({
    required this.title,
    required this.artist,
    required this.coverUrl,
    required this.lyrics,
    required this.colorValue,
    required this.bvid,
    required this.cid,
  });
}
